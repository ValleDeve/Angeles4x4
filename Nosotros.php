    <div class="historia">
        <h1>Historia</h1>
        <p>"Ángeles4x4" fue una idea que germinó hace muchos años en el corazón del municipio de Belén de Umbría. Este encantador pueblo, ubicado en medio de las montañas y rodeado de exuberante vegetación, siempre había sido conocido por su espíritu comunitario y su amor por la naturaleza. Sin embargo, el deseo de hacer algo más grande, algo que uniera a la comunidad en una causa solidaria, fue tomando forma poco a poco.</p>
        <p>A lo largo de los años, Asdrubal Betanncourt empezo a notar la creciente necesidad de ayuda en su comunidad. Muchas familias luchaban contra la adversidad económica, y algunos de sus miembros más vulnerables, como los niños y los ancianos, necesitaban apoyo adicional. La necesidad de un cambio positivo se volvió más evidente con el tiempo.</p>
        <p>Fue entonces cuando el comenzó a reunirse en torno a la idea de "Ángeles4x4". La visión era clara: crear una organización que pudiera brindar asistencia a las personas necesitadas dentro del municipio y, al mismo tiempo, promover un sentido de unidad y solidaridad en la comunidad. El nombre "Ángeles4x4" se inspiró en la idea de Apocalípsis 14:6 por los Ángeles que deben predicar el mensaje a todo el mundo, y 4x4 porque es a todo terreno, osea Nación, tribu pueblo y lengua.</p>
        <p>La creación de la organización no fue tarea fácil. Hubo caidas, debates y planificación meticulosa durante varios años. Se redactaron estatutos y se buscaron fuentes de financiamiento. Cada persona que quisiera colaborar lo hacia, ya fuera con tiempo, habilidades o recursos, para que Ángeles4x4 se convirtiera en una realidad.</p>
        <p>Lo más importante es que Ángeles4x4 ha logrado su objetivo principal: unir a la comunidad en torno a una causa solidaria. Trabajando juntos para hacer del mundo un lugar mejor. La idea que surgió hace tanto tiempo ha florecido y ha demostrado que cuando una comunidad se une, puede lograr cosas maravillosas.</p>
        <p>En resumen, "Ángeles4x4" es mucho más que una organización benéfica; es la encarnación del espíritu santo y un ejemplo inspirador de cómo una idea puede convertirse en una fuerza positiva que cambia vidas y comunidades enteras.</p>

